package com.example.mvvmloginproject.home.model;

public class HomeDataModel {

    public String first_name;
    public String last_name;
    public String email;
    public String number;

    public HomeDataModel(String first_name, String last_name, String email, String number) {
        this.first_name = first_name;
        this.last_name = last_name;
        this.email = email;
        this.number = number;
    }
}
